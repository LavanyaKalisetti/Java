/*
 Java Program to print Multiplication of two floating point Number.
 */
package gfg.programs;

public class ProductOfFloat {
	public static void main(String args[]) {
		float f1=10.0f; // or float f1=(float)10.0;
		float f2=20.0f;
		
		float f=f1*f2;
		System.out.println("The product Float is"+"\s"+f);
		
	}

}
