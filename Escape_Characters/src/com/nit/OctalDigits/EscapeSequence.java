package com.nit.OctalDigits;

public class EscapeSequence {
	public static void main(String[] args) {
		System.out.println("Hi\f Hello");
		System.out.println("Hi Hello\f");
		System.out.println("\f HiHello");
		System.out.println("HRU?");
		System.out.println();
		System.out.println("Hi\bHello");
		System.out.println();
		System.out.println("Hi\nHello");
		System.out.println();
		System.out.println("Hi\rHello");
		System.out.println("HiHello\rHRU?");
		System.out.println("HiHelloHRU?\r");
		System.out.println("\rHiHelloHRU?");
		System.out.println();
		//System.out.println(""");System.out.println(''');
		System.out.println("\"");
		System.out.println('\'');
		System.out.println('"');
		System.out.println("");
		//System.out.println('\');
		//System.out.println("\");
		System.out.println("\\");
		//System.out.println("\\\");"
		System.out.println("\\\\");
	
}
}

